#include "libft.h"

int		ft_len_char(char const *s, char c)
{
	char *p;

	if (!*s)
		return (0);
	while (*s == c && *s)
		s++;
	p = (char *)s;
	while (*p != c && *p)
		p++;
	return (p - s);
}
